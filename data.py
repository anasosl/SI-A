def get_number(string):
    try: 
        string = string.split(" ")
        if (string[1][0] == "E" or string[1][0] == "e"):
            string = string[1][1:]
        else:
            ans = int(string[1]) 
    except:
        if (string[0] == "E" or string[0] == "e"):
            ans = int(string[1:])

    return int(ans)